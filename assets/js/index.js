const buildKeyboard = (chars) => {
  const charTags = chars
    .split("")
    .reduce(
      (acu, char) =>
        acu + `<span onClick='' role="img" aria-label="${char}">${char}</span>`,
      ""
    );
  return `<p>${charTags}</p>`;
};

const loadChars = () => {
  const emojiSelected = document.querySelector(".selected span").innerText;
  switch (emojiSelected) {
    case "😀":
      keyboard.innerHTML = buildKeyboard(smileysPeople);
      break;
    case "🐶":
      keyboard.innerHTML = buildKeyboard(animalsNature);
      break;
    case "🍏":
      keyboard.innerHTML = buildKeyboard(foodDrink);
      break;
    case "⚽":
      keyboard.innerHTML = buildKeyboard(activities);
      break;
    case "🚗":
      keyboard.innerHTML = buildKeyboard(travelPlaces);
      break;
    case "⌚":
      keyboard.innerHTML = buildKeyboard(objects);
      break;
    case "☮":
      keyboard.innerHTML = buildKeyboard(symbols);
      break;
    case "🏳":
      keyboard.innerHTML = buildKeyboard(flags);
      break;
  }
};

const onSelect = (selected) => {
  const currentSelected = document.getElementsByClassName("selected")[0];
  if (currentSelected) {
    currentSelected.classList.remove("selected");
  }
  document.getElementById(selected).classList.add("selected");
  loadChars();
};

window.onload = () => loadChars();
